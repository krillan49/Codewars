# A Leyland number is a number of the form x**2 + y**2 where x and y are integers greater than 1.
#
# The first few such numbers are:
# 8 = 2**2 + 2**2
# 17 = 2**3 + 3**2
# 32 = 2**4 + 4**2
# 54 = 3**3 + 3**3
#
# Return all Leyland numbers up to 1012 as a list, in ascending order.
#
# Your code can be maximum 80 characters long.

#===============================================================================================================
#                                               Solution
#===============================================================================================================

def f
(2..40).map{|x| (2..40).map{|y| x**y+y**x}}.flatten.uniq.sort[0..120]
end
