# We need prime numbers and we need them now!
#
# Write a method that takes a maximum bound and returns all primes up to and including the maximum bound.
#
# For example,
#
# 11 => [2, 3, 5, 7, 11]

#===============================================================================================================
#                                               Solution
#===============================================================================================================

require 'prime'

def prime(n)
  Prime.entries(n)
end
